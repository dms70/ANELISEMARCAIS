<?php

namespace App\Entity;

use App\Repository\CategoryRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity(repositoryClass=CategoryRepository::class)
 */
class Category
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $title;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $description;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $slug;

    /**
     * @ORM\ManyToMany(targetEntity=Makeup::class, mappedBy="categories")
     */
    private $makeups;

    public function __construct()
    {
        $this->makeups = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

    public function setDescription(string $description): self
    {
        $this->description = $description;

        return $this;
    }

    public function getSlug(): ?string
    {
        return $this->slug;
    }

    public function setSlug(string $slug): self
    {
        $this->slug = $slug;

        return $this;
    }

    /**
     * @return Collection|Makeup[]
     */
    public function getMakeups(): Collection
    {
        return $this->makeups;
    }

    public function addMakeup(Makeup $makeup): self
    {
        if (!$this->makeups->contains($makeup)) {
            $this->makeups[] = $makeup;
            $makeup->addCategory($this);
        }

        return $this;
    }

    public function removeMakeup(Makeup $makeup): self
    {
        if ($this->makeups->removeElement($makeup)) {
            $makeup->removeCategory($this);
        }

        return $this;
    }
}
